package com.dalendev.meteotndata.domain;

import com.dalendev.meteotndata.generated.WeatherStation;
import com.googlecode.objectify.annotation.Entity;
import com.googlecode.objectify.annotation.Id;
import com.googlecode.objectify.annotation.Index;
import java.io.Serializable;

/**
 *
 * @author danieleorler
 */
@Entity
public class Station implements Serializable
{
    @Id
    private Long id;
    @Index
    private String code;
    private String name;
    private String shortName;
    private int altitude;
    private float latitude;
    private float longitude;
    private long lastUpdate;

    public Station()
    {
    }
    
    public Station(WeatherStation ws)
    {
        this.code = ws.getCode();
        this.name = ws.getName();
        this.shortName = ws.getShortName();
        this.altitude = ws.getAltitude().intValue();
        this.latitude = ws.getLatitude().floatValue();
        this.longitude = ws.getLongitude().floatValue();
        this.lastUpdate = 0;
    }
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getShortName() {
        return shortName;
    }

    public void setShortName(String shortName) {
        this.shortName = shortName;
    }

    public int getAltitude() {
        return altitude;
    }

    public void setAltitude(int altitude) {
        this.altitude = altitude;
    }

    public float getLatitude() {
        return latitude;
    }

    public void setLatitude(float latitude) {
        this.latitude = latitude;
    }

    public float getLongitude() {
        return longitude;
    }

    public void setLongitude(float longitude) {
        this.longitude = longitude;
    }

    public long getLastUpdate() {
        return lastUpdate;
    }

    public void setLastUpdate(long lastUpdate) {
        this.lastUpdate = lastUpdate;
    }

}
